<head>
    <meta charset="UTF-8">
    <title>Login</title>
    <style>
        body { font: 14px sans-serif; background-color: #333; color: #fff; }
        .wrapper { width: 360px; padding: 20px; margin: auto; background-color: #444; border-radius: 10px; }
        .form-group { margin-bottom: 1em; }
        .form-group label { display: block; }
        .form-group input[type="text"],
        .form-group input[type="password"] { width: 100%; padding: 0.5em; }
        .btn { background-color: #555; color: #fff; padding: 0.5em 1em; border: none; cursor: pointer; }
        .error { color: #ff0000; }
    </style>
</head>
<body>
    <div class="wrapper">
        <h2>Login</h2>
        <p>Please fill in your credentials to login.</p>
        
        <?php 
        if (!empty($login_err)) {
            echo '<div class="error">' . $login_err . '</div>';
        }        
        ?>

        <form action="<?php echo htmlspecialchars($_SERVER["PHP_SELF"]); ?>" method="post">
            <div class="form-group">
                <label>Username</label>
                <input type="text" name="username" class="<?php echo (!empty($username_err)) ? 'is-invalid' : ''; ?>" value="<?php echo $username; ?>">
                <span class="error"><?php echo $username_err; ?></span>
            </div>    
            <div class="form-group">
                <label>Password</label>
                <input type="password" name="password" class="<?php echo (!empty($password_err)) ? 'is-invalid' : ''; ?>">
                <span class="error"><?php echo $password_err; ?></span>
            </div>
            <div class="form-group">
                <input type="submit" class="btn" value="Login">
            </div>
        </form>
    </div>    
</body>
